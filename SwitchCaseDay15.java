package pitta;

public class SwitchCaseDay15 {

    public static void main(String[] args) {

        int hari = 6;
        switch (hari) {
            case 1:
                System.out.println("Hari ini saya makan tempe");
                break;
            case 2:
                System.out.println("Hari ini saya minum susu");
                break;
            case 3:
                System.out.println("Hari ini saya jalan-jalan");
                break;
            case 4:
                System.out.println("Hari ini saya makan nasi goreng");
                break;
            case 5:
                System.out.println("Hari ini saya minum jus");
                break;
            case 6:
                System.out.println("Hari ini adalah senin");

        }
    }
}
